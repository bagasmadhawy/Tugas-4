package com.example.setelahgagal;

public class ModelUlar {
    private String namaUlar;
    private String detailUlar;
    private int gambarUlar;

    public String getSnakeName() {
        return namaUlar;
    }

    public void setSnakeName(String namaUlar) {
        this.namaUlar = namaUlar;
    }

    public String getSnakeDetail() {
        return detailUlar;
    }

    public void setSnakeDetail(String detailUlar) {
        this.detailUlar = detailUlar;
    }

    public int getSnakeImage() {
        return gambarUlar;
    }

    public void setSnakeImage(int gambarUlar) {
        this.gambarUlar = gambarUlar;
    }
}

